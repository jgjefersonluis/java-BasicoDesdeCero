package ciclos;

public class SintaxisWhile {

    /**
     *
     * while(condicion(es) { instrucciones; }
     *
     *
     *
     * @param args
     */

    public static void main(String args []) {
        int c = 1;

        while (c <= 10) {
            System.out.println("Hola, mundo!" + c);
            c++;
        }
    }

}
