package poo;

public class TeoriaPoo {

    public static void main(String[] args) {
        /*
         Teoría POO
         PROGRAMACIÓN ORIENTADA A OBJETO 

         La programación orientada a objetos es un paradigma de programación dicho con otras palabras es una forma de programar. Dentro de la programación importantes que debes entender:

         1.- Metodo
         2.- Clase
         3.- Constructor
         4.- Objetos (instancia)
         5.- Encapsulamineto
         6.- Herencia
         7.- Polimorfismo
         */

        /*
    
         1.- METODO
         Los métodos son módulos de código que se pueden mandar a llamar.
         Los métodos se utilizan para reducir la cantidad de código que escribes esto te permite mantener tu programa con una mayor facilidad. En POO tenemos 2 tipos de métodos GETS y SETS, los métodos GET nos sirven para obtener valores que ya tenga la instancia y los métodos SET para enviar valores a los atributos de la clase.

      
         2.- CLASE
         Las clases son el conjunto de métodos que se dedican a realizar una función especifica, cada clase puede tener métodos propios y heredados o solo métodos propios. Las clases las podríamos identificar como un plano de una casa, a partir de un solo plano se pueden construir mil casas, las clases cumplen una función similar a partir de una clase podemos construir mil objetos (instancias).
      
         3.- CONSTRUCTOR
         Los constructores son métodos especiales que nos sirven para inicializar valores desde que se crea el objeto (instancia) de la clase, al ser un método también puede existir una sobre carga de constructores. La declaración de un constructor es similar a la de cualquier método, lo especial en la declaración de los constructores es que estos deben de tener como nombre del constructor el mismo nombre de la clase. Por ejemplo imaginemos que tenemos una clase llamada personas, el constructor de esa clase quedaría de la siguiente manera:
      
         public Personas(TD par1, TD par2, TD par3,...)
         {
      
         }
      
         Una clase puede no tener un método constructor y funcionar de forma correcta sin ningún problema.
         Nota.- Siempre que nosotros creamos una instancia de la clase se auto ejecuta de manera automática el método constructor.
         Cosas importantes de los constructores
         1.- Los constructores no se heredan.
         2.- Los constructores no regresan ningún valor, es el único método que a pesar de eso no se le coloca VOID.
         3.- Debe de declararse siempre publico para que pueda ser accedido desde cualquier parte de nuestro código.
      
         4.- OBJETO (INSTANCIA)
         Un objeto o también llamado instancia de clase se utiliza para crear una referencia de la clase que se genere ese objeto y de esa manera poder ocupar los métodos y atributos (PÚBLICOS) que contenga esta clase. El crear una instancia en otras palabras es crear una copia para cada una de ellas con toda la información de la clase.
      
         5.- ENCAPSULAMINETO
         El encapsulamineto nos permite aislar ciertos elementos de nuestra clase para que solo funcionen dentro de ella. Tenemos algo que denominamos modificadores de acceso, estos nos sirven para indicarle a la clase que elementos que la contengan van a ser de acceso publico y cuales no.
         Los modificadores de acceso son 3:
         Public.- Se usa para darle acceso desde cualquier parte de la estructura de nuestro proyecto, dicho de otra manera cualquier clase puede acceder a él.
         Protected.- Podemos acceder desde la misma clase, clases que estén en el mismo paquete y clases que hereden de ella.
         Private.- Unicamente se puede acceder desde la misma clase.
      
         Si nosotros no especificamos un modificador coloca uno por defecto con las siguientes características: es accesible para la clase y para el paquete pero no para las subclases que pudieran existir.
     
         6.- HERENCIA
         La herencia nos sirve para que una nueva clase adquiera los métodos y atributos de una existente, adicionalmente tener más métodos para potenciar su trabajo.
     
         7.- POLIMORFISMO
         El poliformismo nos permite programar en forma general en vez de especifica. En particular nos permite escribir programas que procedan de una superclase (ya sea de forma directa o indirecta) como si todos fueran objetos de la superclase; esto puede simplificar la programación.
    
         }
         */
    }

}
